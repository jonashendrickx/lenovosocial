package com.lenovo.social.release;

public class Product {
		private String name;
		private String url;
		
		public Product() {
			
		}
		
		public void setName(String name) {
			this.name = name;
		}
		
		public void setURL(String url) {
			this.url = url;
		}
		
		public String getName() {
			return name;
		}
		
		public String getURL() {
			return url;
		}
	}